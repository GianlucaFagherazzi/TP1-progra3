public class Ej11 {
    public static void main(String[] args) {
        int numero = 100;  // Iniciamos la variable con el valor 100
        
        // Utilizamos la estructura do..while para decrementar y mostrar los números
        do {
            System.out.println(numero);
            numero--;  // Decrementamos el valor de la variable
        } while (numero >= 1);  // Condición para continuar el ciclo
    }
}
